class Provider(str, Enum):
    """The coding-agent backend that powers a session.

    Subclassing ``str`` keeps JSON serialisation simple and makes
    comparisons with literal strings ergonomic in tests.
    """

    CLAUDE_CODE = "claude_code"
    OPEN_CODE = "open_code"


class SessionStatus(str, Enum):
    """State machine for :class:`CodingSession`.

    Allowed transitions (see :mod:`qai.ai_coding.domain.entities`):

    .. code-block:: text

        PENDING --> ACTIVE --> STREAMING --> ACTIVE
                |       |        |
                |       |        +--> PERMISSION_REQUESTED --> ACTIVE
                |       v
                |    IDLE --> ACTIVE
                v
            TERMINATED
    """

    PENDING = "pending"
    ACTIVE = "active"
    IDLE = "idle"
    STREAMING = "streaming"
    PERMISSION_REQUESTED = "permission_requested"
    TERMINATED = "terminated"


class PermissionDecision(str, Enum):
    """Outcome of a :class:`PermissionRequest`.

    ``PENDING`` is the implicit initial state and is also exposed as a
    distinct value so the persistence layer can serialise it directly.
    """

    PENDING = "pending"
    APPROVED = "approved"
    REJECTED = "rejected"


class StreamFrameKind(str, Enum):
    """Logical kind of a streaming frame surfaced to the caller."""

    TEXT = "text"
    TOOL_CALL = "tool_call"
    TOOL_RESULT = "tool_result"
    PERMISSION_REQUEST = "permission_request"
    ERROR = "error"
    END = "end"
    # Task/Agent sub-task frames (V1 parity: session_manager.py:2044-2079).
    # Tail-appended — does not change existing values/ordering, so the
    # locked SSE frame format (§3.1) is preserved. Like PERMISSION_REQUEST,
    # the native Anthropic HTTP SSE wire does not emit these today; they are
    # surfaced when an upstream agent runtime injects task_started /
    # task_progress / task_notification events (the front-end builds the V1
    # "🤖 sub-tasks" cards from them).
    TASK_STARTED = "task_started"
    TASK_PROGRESS = "task_progress"
    TASK_NOTIFICATION = "task_notification"


