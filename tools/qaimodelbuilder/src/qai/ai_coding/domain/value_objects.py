"""Value objects for the ai_coding bounded context.

This module gathers the immutable, equality-by-value building blocks of
the AI coding domain.  Following the S2 conventions (see
``docs/90-refactor/S2-sub-agent-spec.md`` §4.1) every value object is a
``@dataclass(frozen=True, slots=True, kw_only=True)`` so callers cannot
accidentally mutate identifiers, paths or message payloads.

Design notes
------------
* The :class:`Provider` enum unifies what was previously two parallel
  implementations (``ClaudeCodeSessionManager`` and the OpenCode session
  manager) into a single aggregate.  The choice of provider is captured
  on :class:`CodingSession`; it does NOT trigger any new
  cross-implementation duplication.
* :class:`Workspace` carries an absolute filesystem path string but does
  not perform any I/O — the domain layer must remain pure.  Validation
  is delegated to ``qai.platform.io_validator``.
* :class:`CodingStreamFrame` is the wire-shape of a single SSE frame
  emitted by the streaming use case.  It is intentionally provider-
  agnostic so the chat / app_builder contexts can adopt it later.
"""

