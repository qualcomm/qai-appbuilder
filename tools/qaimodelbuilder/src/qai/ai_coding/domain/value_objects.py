@dataclass(frozen=True, slots=True, kw_only=True)
class CodingStreamFrame:
    """One frame yielded by a streaming coding session.

    ``payload`` is intentionally typed as ``dict[str, Any]`` rather than
    a tagged union: each :class:`StreamFrameKind` carries a different
    payload shape and the application layer is responsible for
    interpreting it.  This keeps the domain contract minimal and lets
    the SSE adapter forward frames verbatim.
    """

    kind: StreamFrameKind
    payload: dict[str, Any]
    sequence: int

    def __post_init__(self) -> None:
        if self.sequence < 0:
            raise ValueError("sequence must be >= 0")


